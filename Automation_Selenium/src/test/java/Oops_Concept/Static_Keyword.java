package Oops_Concept;

public class Static_Keyword {
	
	
	/**
	 * Static Keyword
	 * Static method belong to Class(Type) and not to object.
	 * 
	 */
      static String name="ABC";
      byte age=6;
      String grade="1st";
      
      public void displaydetails()
      {
    	  System.out.println("Student name is "+name+" with age "+age+ " with grade "+grade);
      }
}
